#ifndef Renderable_h
#define Renderable_h

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////
#include <SimpleEngine/myImports.hpp>
namespace se
{
    ////////////////////////////////////////////////////////////
    /// Abstract class for renderable elements
    ////////////////////////////////////////////////////////////
    class SE_API Renderable
    {
        public:

        ////////////////////////////////////////////////////////////
        /// This method defines how the element is rendered.
        /// It must be defined later
        ////////////////////////////////////////////////////////////
        virtual void render() = 0;

        virtual void renderLogic() = 0;

        ////////////////////////////////////////////////////////////
        /// This method defines how the element is to be interpolated.
        /// It must be defined later
        ////////////////////////////////////////////////////////////
        virtual void interpolate(const float interpolation) = 0;

        virtual bool unregister()  const = 0;
    };
}
#endif
